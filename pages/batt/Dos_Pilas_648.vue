          <h4>開局</h4>
          <ul>
<li>玩家將從6位鷹勇士，1位精銳鷹勇士，4位羽毛箭射手開始。首先會看到在淺灘被卡拉克其爾的部隊屠殺的雙柱城的軍隊，玩家此時快速集結部隊並向南前往並接管雙柱城，同時試圖盡可能地避開先鋒突襲隊。</li>
<li>玩家必須在沒有失去市鎮中心的情況下保衛雙柱城，同時面對卡拉克其爾和先鋒突擊隊的入侵。請將羽弓手進駐箭塔裡並優先控箭塔殺矛兵和投石兵等遠距單位，鷹勇士守在外面，因為先後有兩台先鋒突擊隊的衝車從北方和西南方進攻。</li>
<li>家裡則繼續維持經濟，如果可能最好派多一點人去採集火雞店右下角和烏克夏吞人的石頭。</li>
<li>遊戲時間過了一定時間(難度困難為20分鐘，標準難度為30分鐘)會自動升到城堡時代，一旦玩家進入城堡時代，卡拉克其爾將會有一支龐大的軍隊入侵雙柱城。一旦他們走近雙柱城的大門，玩家必須此時抉擇要繼續忠於蒂卡爾皇室還是投誠到卡拉克其爾。</li>
<li>請注意，此時從另一個玩家接管雙柱城的建築物不影響升級，因此即使研發所有與箭塔有關的科技，從雙柱城拿到的最初的瞭望箭塔也無法升級。</li>
        </ul>
          <h4>A:選擇投誠卡拉克其爾</h4>
          <ul>
<li>如果玩家選擇加入卡拉克其爾的一方，玩家將接管雙柱城。卡拉克其爾的入侵部隊也將會從雙柱城中撤退。</li>
<li>玩家必須防禦烏夏克吞城和蒂卡爾人的入侵，烏夏克吞城通常會有一批長劍兵，長槍兵和弩兵組成的的輕裝部隊，蒂卡爾則是會有弩兵、戰矛兵、投石手和衝車的混合部隊以波狀的方式一直出擊，基本上是個長期抗戰。</li>
<li>玩家可以在敵人常出沒的地方建造建塔甚至城堡防守，但是請注意敵人的AI很刁鑽，有時後甚至會繞過防線從防守薄弱的地方進攻或是騷擾你的村民。</li>
<li>本關礦源稀少（難度困難的情況下甚至在初期只在左下角給你一個金礦，除非奪取了先鋒突擊度的基地），初期在敵人波狀攻擊之下很難用黃金兵種一直撐，也很難有餘裕先行掃蕩先鋒突擊隊的兵力以及解放市集取得黃金，所以應對的兵種以大量戰矛兵為主輔以少量鷹勇士，直到穩定取得黃金的來源為止。</li>
<li>由於敵人的時代是跟著玩家的，請注意在準備好之前先不要升帝王，建議要進攻蒂卡爾之前再升。</li>
<li>與防守良好完善的蒂卡爾城相比，烏夏克吞城相對容易進攻，玩家如果越早取得此座城池建立基地，從中路前往蒂卡爾城的進攻會更快速，同時也少一國打你。納蘭永人不會主動攻擊玩家，因此可以不用去理。</li>
<li>由於此關無法建造巨型投石機，玩家必須依靠重型衝撞車，並且讓步兵完全進駐至衝撞車內會提高速度。後期遠距兵種以強弩兵為主而非羽弓，搭配銀冠科技黑曜石之箭協助衝車拆建築，一隊強弩在衝車的掩護之下可以豪髮無傷地拆掉劍塔城堡，輔以搭配研發黃金國的精銳鷹勇士，一步一步瓦解敵人的基地，最後直撲世界奇觀。</li>
          </ul>
          <h4>B:繼續效忠蒂卡爾</h4>
          <ul>
<li>如果玩家選擇忠於蒂卡爾一方，整個雙柱城，以及由玩家建造的所有建築物，將被轉移給黃色“雙柱城”玩家，並且玩家將在雙柱城的東門產生一位鷹勇士和4位村民並逃往烏夏克吞城。一旦玩家的單位到達烏夏克吞城，玩家將接管整個城市，雙柱城將轉移黃色並將外交關係改成敵對。玩家可以選擇摧毀雙柱城的城鎮中心以並重新奪回這座城市，儘管沒有攻打雙柱城也能獲勝。</li>
<li>如果玩家想省麻煩，可以用偷吃步的方式─再選擇世界奇觀前把除了城鎮中心的建築物刪掉，用木牆把城鎮中心圍起來讓雙柱城無法發展，外面留個市集方面貿易。</li>
<li>在卡拉克其爾入侵期間，玩家可以製造衝撞車裝載步兵，弓兵與矛兵在後掩護，並在攻破城門直搗雙柱城的城鎮中心，只需把注意力放在城鎮中心即可，攻破城鎮中心後雙柱城的建築回歸玩家。卡拉克其爾軍隊將不停的入侵烏夏克吞城，如果雙柱城在玩家接管後，他們偶爾也會進攻。</li>
<li>如果此關玩家進行超過3800秒（63分20秒），玩家探索地圖發現納蘭永城時，玩家如果擁有至少70個軍事單位，玩家被要求從卡拉克其爾的入侵中保衛納蘭永城15分鐘，否則納蘭永城將被卡拉克其爾俘虜並建立自己的軍事力量與玩家外交關係改為敵對交戰。在這15分鐘內，如果卡拉克其爾有至少8個單位進入納蘭永城，而納蘭永城只有5個或少於5個的軍事單位，納蘭永城被認為被卡拉克其爾“俘虜”並將叛逃到卡拉克其爾的一方。然後玩家將被要求擊敗納蘭永城。反之，一旦成功防守，納蘭永城會是繼續防衛卡拉克其爾的力量，可以幫助玩家在東部防禦卡拉克其爾。</li>
<li>一旦防衛納蘭永城的任務開始，納蘭永上方的城門會自動爆掉，玩家應該立刻補建，然後在裡面插堡以及建造軍事建築防禦敵人的進攻。困難難度下敵人的進攻非常猛烈，甚至有不少衝車，必須帶3~4投石車從面往外瓦解敵人的衝車和圍城部隊。</li>
<li>其餘的進攻事項和上述打蒂卡爾的攻略相同。</li>
          </ul>
